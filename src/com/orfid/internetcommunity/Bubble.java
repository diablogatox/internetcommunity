package com.orfid.internetcommunity;

public class Bubble {

	private String uid;
	private String username;
	private String photo;
	private String bubble_type;
	private String bubble_content;
	private String utime;
	
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPhoto() {
		return photo;
	}
	public void setPhoto(String photo) {
		this.photo = photo;
	}
	public String getBubble_type() {
		return bubble_type;
	}
	public void setBubble_type(String bubble_type) {
		this.bubble_type = bubble_type;
	}
	public String getBubble_content() {
		return bubble_content;
	}
	public void setBubble_content(String bubble_content) {
		this.bubble_content = bubble_content;
	}
	public String getUtime() {
		return utime;
	}
	public void setUtime(String utime) {
		this.utime = utime;
	}
}
